    // Fonction pour la recherche 

    public function findFilter($filter)
    {
        /* CrÃ©ation d'une requete avec un filtre 
        
            de recherche par valuer saise dans le champ de saisie 
        */

        $query= $this->createQueryBuilder('p')
            ->select("p", "c")
            ->join("p.categorie", "c");

        if($filter)
        {
            $query = $query
                ->andWhere("p.titre LIKE :search")
                ->orWhere("p.prix LIKE :search")
                ->orWhere("p.description LIKE :search")
                ->orWhere("c.titre LIKE :search")
                ->setParameter("search", "%{$filter}%");
        }
        
        return $query
        ->getQuery()
        ->getResult();
    }
